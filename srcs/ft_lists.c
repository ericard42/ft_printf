int     type_list(int c)
{
    return ((c == 'c') || (c = 's') || (c == 'p') || (c == 'd') || c == 'i'
            || (c == 'u') || (c == 'x') || (c == 'X') || (c == '%'));
}

int     flag_list(int c)
{
    return ((c == '-') || (c == '0') || (c == '.') || (c == '*'));
}